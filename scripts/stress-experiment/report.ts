import type { StressMetrics } from './metrics'
import { computeMetrics } from './metrics'
import { STRESS_SCENARIO } from './scenario'

/**
 * The DERIVED stress-experiment report (T26, ADR-0039) — the T25 Layer-2 pattern: a docs view whose
 * every number comes from `computeMetrics(STRESS_SCENARIO)` over the real T23/T24 sources, never typed
 * by hand. The committed `docs/stress-experiment.md` must be byte-identical to `renderStressReport()`;
 * `pnpm stress:verify` reds on an out-of-band edit (the same byte-gate discipline as render-stats /
 * render-claims / matrix:render). The sidecar (`renderSidecar`) carries the same metric values plus a
 * wall-clock stamp; the DOC excludes the stamp so it stays byte-stable until a real number changes.
 */

const START =
  '<!-- stress:start (generated by `pnpm stress:render`; do not edit — `pnpm stress:verify` gates drift) -->'
const END = '<!-- stress:end -->'

export const STRESS_DOC_START = START
export const STRESS_DOC_END = END

const pct = (n: number): string => `${(n * 100).toFixed(1)}%`
const ratio2 = (n: number): string => n.toFixed(4)

function renderMetricsBlock(m: StressMetrics): string {
  const c = m.cheatRate
  const fg = m.falseGreen
  const hi = m.humanIntervention
  const drift = m.anchoredDrift
  const culpritRows =
    m.culprits.length === 0
      ? '_no bisected culprit in the prepared ledger_'
      : m.culprits
          .map(
            (r) =>
              `\`${r.batchFrom}…${r.batchTo}\` → \`${r.culpritSha}\` in ${r.commits} bisection commit(s), ${Math.round(r.wallMs / 60_000)} min wall-time`,
          )
          .join('; ')

  const lines: string[] = []
  lines.push(START)
  lines.push('')
  lines.push('| # | Metric | Value | Real source |')
  lines.push('|---|---|---|---|')
  lines.push(
    `| 1 | cheat-rate | ${pct(c.rate)} (${c.cheated}/${c.total}); ${c.detected}/${c.cheated} caught (${pct(c.detectionRate)}) | T23 attack taxonomy + gate-guard gate↔target detector |`,
  )
  lines.push(
    `| 2 | false-green rate | ${pct(fg.rate)} (${fg.falseGreens}/${fg.presubmitGreens} presubmit-greens) | T24 promotion ledger (PRESUBMIT_GREEN later RED) |`,
  )
  lines.push(
    `| 3 | time-to-culprit | ${culpritRows} | T24 promotion ledger (batch red → bisected culprit, O(log n)) |`,
  )
  lines.push(
    `| 4 | human-intervention rate | ${pct(hi.rate)} (${hi.interventions}/${hi.total}; ${hi.frozen} freeze-and-page, ${hi.flagged} flagged) | T24 change classifier + T23 abort affordance |`,
  )
  lines.push(
    `| 5 | anchored-ratio drift | ${ratio2(drift.drift)} (start ${ratio2(drift.start)} → end ${ratio2(drift.end)}; ${m.anchored.anchored}/${m.anchored.anchorable} boundaries anchored) | T23 anchored-severity coverage |`,
  )
  lines.push('')
  lines.push(
    `Cheated features: ${c.cheatedIds.length === 0 ? '_none_' : c.cheatedIds.map((id) => `\`${id}\``).join(', ')}. ` +
      `False greens: ${fg.falseGreenShas.length === 0 ? '_none_' : fg.falseGreenShas.map((s) => `\`${s}\``).join(', ')}. ` +
      `Required a human: ${hi.interventionIds.length === 0 ? '_none_' : hi.interventionIds.map((id) => `\`${id}\``).join(', ')}.`,
  )
  lines.push('')
  lines.push(END)
  return lines.join('\n')
}

function renderPlantedCheatBlock(m: StressMetrics): string {
  const p = m.plantedCheat
  const real = p.realTheater ? 'real theater (a weak check greens it)' : 'NOT theater — review'
  const caught = p.caught ? 'caught (the named defense reds it)' : 'NOT caught — P0 finding'
  return [
    '## The single planted cheat (independent-party framing)',
    '',
    `One adversarial cheat is staged on feature \`${p.featureId}\`: the **${p.attack}** attack from the` +
      ' T23 taxonomy (an `__eq__`→True / assertion-less test — the ImpossibleBench GPT-5 move). It is',
    `proven caught by an AGENT-INDEPENDENT **Tier-0 falsifier**, the existing claim` +
      ` [\`${p.falsifierClaimId}\`](claims.md) — no new claim was added (the T25 anti-theater lesson:` +
      ' reuse a falsifier that already demonstrates it).',
    '',
    '```bash',
    `pnpm prove ${p.falsifierClaimId} --break   # arms ${p.toggle}=1, re-runs the gate — it must RED`,
    '```',
    '',
    `- falsifier resolves in the manifest: **${p.falsifierResolves ? 'yes' : 'NO'}**`,
    `- the staged cheat is ${real}`,
    `- the named defense (in-process mutation kill ratio) reds it: ${caught}`,
    '',
    'The cheat greens a weak check but the mutation gate kills it — oracle strength, not a green check,' +
      ' is what defends the boundary.',
  ].join('\n')
}

/** The full committed report. Deterministic: every number derives from `computeMetrics`. */
export function renderStressReport(
  metrics: StressMetrics = computeMetrics(STRESS_SCENARIO),
): string {
  const md: string[] = []
  md.push('# The 100-feature agentic stress test (T26)')
  md.push('')
  md.push(
    '> Generated by `pnpm stress:render` from `scripts/stress-experiment/` over the real T23/T24' +
      ' sources; do not edit by hand (`pnpm stress:verify` gates drift). See' +
      ' [ADR-0039](adr/0039-the-100-feature-agentic-stress-test.md) for the experiment design and the' +
      ' named limits.',
  )
  md.push('')
  md.push(
    'The premise is the **experiment**, not the survival: this instruments whether QARoom’s' +
      ' check architecture holds under agents, and is designed so it CAN falsify' +
      ' “the architecture survives agents”. Success = the architecture holds; any failure is a' +
      ' P0 finding fed back as a claim (incident→claim, T04).',
  )
  md.push('')
  md.push('## The five instrumented metrics')
  md.push('')
  md.push(
    `Computed over the prepared scenario \`${metrics.scenario}\`. Every value is a real function over a` +
      ' real merged source (the promotion ledger, the attack taxonomy, the change classifier, the' +
      ' anchored-severity coverage) — not a stub.',
  )
  md.push('')
  md.push(renderMetricsBlock(metrics))
  md.push('')
  md.push(renderPlantedCheatBlock(metrics))
  md.push('')
  md.push('## What this experiment does NOT do (the deliverable is naming the limits)')
  md.push('')
  md.push(
    '1. **The full 100-feature run is the scenario this PREPARES FOR, explicitly not executed.** This is' +
      ' the slim scope: it wires the five metrics to their real sources and stages one caught cheat. It' +
      ' does not drive 100 features (a deliberate scope call).',
  )
  md.push(
    '2. **It tests THIS system, under THESE agents, against THIS threat model.** It does not prove' +
      ' general agent safety; the taxonomy is the measured ImpossibleBench / METR / Anthropic record,' +
      ' not every possible attack.',
  )
  md.push(
    '3. **The oracle problem stands** (STORY open problem 1): a maximally severe run of the WRONG' +
      ' property is confidently wrong, faster. These metrics measure whether the checks held, not' +
      ' whether the checks were the right ones.',
  )
  md.push('')
  return `${md.join('\n')}\n`
}

/** The gitignored sidecar evidence: the same metric values plus a wall-clock stamp (excluded from the doc). */
export function renderSidecar(metrics: StressMetrics, generatedAt: string): string {
  return `${JSON.stringify({ schema_version: 1, generated_at: generatedAt, ...metrics }, null, 2)}\n`
}
